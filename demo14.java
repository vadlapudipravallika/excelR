public class demo14{
    public static void main(String[] args) {
        int x = 9;
        System.out.println("preincrement of x: " + ++x);
        System.out.println("x = " + x);

        int y = 8;
        System.out.println("post-increment of y: " + y++);
        System.out.println("y = " + y);

        int z = 7;
        System.out.println("pre=decrement of z: " + --z);
        System.out.println("z = " + z);

        int a = 6;
        System.out.println("post-decrement of a: " + a--);
        System.out.println("a = " + a);

    }
}